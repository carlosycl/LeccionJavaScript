let a=3, b=2;

let z = a+b;
console.log("El resultado de la suma es: " + z);

z= a*b;
console.log("El restultado de la resta es: " + z);

z= a-b;
console.log("El resultado de la resta es: " + z);

z= a**b;
console.log("El restultado del exponente es: " + z);

//resuido 

z= a%b;
console.log("El residuo entre 3 y 2 es: " + z);

//Incremento 
//pre-incremento (El operador "++" antes de la variable) 

z= ++a;
console.log(a);
console.log(z);

//Post-incremento (El operador "++" despues de la variable)

z= a++;
console.log(a);
console.log(z);

//pre-decremento 
z = --a;
console.log(a);
console.log(z);

// post-decremento
z = a--;
console.log(a);
console.log(z);


